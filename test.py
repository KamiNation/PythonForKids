traditional_score = "85"
modern_score = "90"

# string concatenation
combined_score = traditional_score + modern_score

fcombined_score = f"{traditional_score} and {modern_score}"

print(type(fcombined_score))  # This will print <class 'str'>
print("My combined score is:", fcombined_score)


combined_scoreNumber = int(traditional_score) + int(modern_score)

print(type(combined_score))  # This will print <class 'str'>
print("My combined score is:", combined_score)

print(type(combined_scoreNumber))  # This will print <class 'int'>
print("My combined score as number is:", combined_scoreNumber)



